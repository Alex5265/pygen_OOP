class LoopTracker:
    def __init__(self, iterable):
        self.iterable = iter(iterable)
        self._first = self._next = next(self.iterable,None)
        self._accesses = 0
        self._empty_accesses = 0

    def __iter__(self):
        return self

    def __next__(self):
        if self._next is not None:
            self._accesses += 1
            self._last = result = self._next
            self._next = next(self.iterable,None)
            return result
        else:
            self._empty_accesses +=1
            raise StopIteration

    @property
    def accesses(self):
        return self._accesses

    @property
    def empty_accesses(self):
        return self._empty_accesses

    @property
    def first(self):
        if self._first is not None:
            return self._first
        else:
            raise AttributeError('–ò—Å—Ö–æ–¥–Ω—ã–π –∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã–π –æ–±—ä–µ–∫—Ç –ø—É—Å—Ç')

    @property
    def last(self):
        try:
            return self._last
        except:
            raise AttributeError ('–ü–æ—Å–ª–µ–¥–Ω–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –Ω–µ—Ç')

    def is_empty(self):
        if self._next:
            return False
        return True





